def count(arr)
  return arr.count { |element| element != 0 } 
end

def missile
  # —Ç–∏–ø —Å–Ω–∞—Ä—è–¥a
  missile = [ ">", "#","‚ò†","‚öïÔ∏è","‚úàÔ∏è","‚û°Ô∏è","‚ôÇÔ∏è","‚ôÄÔ∏è","‚òÄ","‚ô¶","‚úâ","‚úè","‚úí"\
