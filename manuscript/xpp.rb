def shoot_bar(l_name, l_health, l_count, \
              r_name, r_health, r_count, \
              to_right )
  # —Ä–∞—Å—á–µ—Ç –¥–ª–∏–Ω—ã –ø–æ–ª—è –ª–µ–≤—ã—Ö
  l_health = "100%"                       # –∑–¥–æ—Ä–æ–≤—å–µ
  l_position = l_name.to_s + " #{l_count.to_s}u " +\
                "#{l_health.to_s}h"
  # ... –∏ –ø—Ä–∞–≤—ã—Ö
  r_health = "100%"                       # –∑–¥–æ—Ä–æ–≤—å–µ
  r_position = r_name.to_s + " #{r_count.to_s}u " +\
                "#{r_health.to_s}h"
  # —Ä–∞—Å—á–µ—Ç –¥–∏—Å—Ç–∞–Ω—Ü–∏–∏ —Å—Ç—Ä–µ–ª—å–±—ã
  distance = 80 - ( l_position.size + r_position.size )
  miss_ile = missile                      # —Ç–∏–ø —Å–Ω–∞—Ä—è–¥–∞

  # –ø–æ–ª–µ—Ç —Å–Ω–∞—Ä—è–¥–∞
  1.upto(distance) do |i|
    fire = i                              # –ø–ª–∞–º—è —Å–Ω–∞—Ä—è–¥–∞
    air = distance - i + 2                # —Å–Ω–∞—Ä—è–¥ –Ω–∞ –¥–≤–∞ —Å–∏–º–≤–æ–ª–∞

    # –∞—Ç–æ–º –¥–≤–∏–∂–µ–Ω–∏—è, –∑–∞—Å—Ç—ã–≤—à–µ–µ –º–≥–Ω–æ–≤–µ–Ω–∏–µ
    case to_right
    when true                             # —Å–ø—Ä–∞–≤–∞ –Ω–∞ –ª–µ–≤–æ 
      print "#{l_position}" + \
            "="*i + "#{miss_ile}" + ">" + " "*air + \
            "#{r_position}" + "\r"
      sleep rand (0.05..0.09)               # —Ç—Ä–µ–Ω–∏–µ –ø–µ—Ä–µ–º–µ–Ω—á–∏–≤–æ
    else
      print "#{l_position}" + \
          " "*air + "<" + "#{miss_ile}" + "="*i + \
          "#{r_position}" + "\r"
      sleep rand (0.05..0.09)               # —Ç—Ä–µ–Ω–∏–µ –ø–µ—Ä–µ–º–µ–Ω—á–∏–≤–æ
    end
  end
end

