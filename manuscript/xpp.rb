def shoot_at( target_arr, \
              l_name, l_health, l_count, \
              r_name, r_health, r_count,
              to_right )
  target = rand(0..9)
  shoot_bar(  l_name, l_health, l_count, \
              r_name, r_health, r_count,\
              to_right )
  if target_arr[ target ] != 0
    target_arr[target] = 0 
    puts "\n  –µ—Å—Ç—å –ø–æ–ø–∞–¥–∞–Ω–∏–µ –Ω–∞ –ø–æ–∑–∏—Ü–∏–∏ #{target} !"
  else
    puts "\n  –ø—Ä–æ–º–∞—Ö..."
  end
end
